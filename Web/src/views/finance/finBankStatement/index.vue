          <template #default="scope">
            <el-tag v-if="scope.row.isMatched"> 是 </el-tag>
            <el-tag type="danger" v-else> 否 </el-tag>
          </template>
        </el-table-column>
        <el-table-column prop='matchedVoucherId' label='匹配凭证ID' show-overflow-tooltip />
        <el-table-column prop='isReconciled' label='是否已对账' show-overflow-tooltip>
          <template #default="scope">
            <el-tag v-if="scope.row.isReconciled"> 是 </el-tag>
            <el-tag type="danger" v-else> 否 </el-tag>
          </template>
        </el-table-column>
        <el-table-column prop='reconcileUserId' label='对账人ID' show-overflow-tooltip />
        <el-table-column prop='reconcileTime' label='对账时间' show-overflow-tooltip />
        <el-table-column prop='remark' label='备注' show-overflow-tooltip />
        <el-table-column label="修改记录" width="100" align="center" show-overflow-tooltip>
          <template #default="scope">
            <ModifyRecord :data="scope.row" />
          </template>
        </el-table-column>
        <el-table-column label="操作" width="140" align="center" fixed="right" show-overflow-tooltip v-if="auth('finBankStatement:update') || auth('finBankStatement:delete')">
          <template #default="scope">
            <el-button icon="ele-Edit" size="small" text type="primary" @click="editDialogRef.openDialog(scope.row, '编辑银行对账单')" v-auth="'finBankStatement:update'"> 编辑 </el-button>
            <el-button icon="ele-Delete" size="small" text type="primary" @click="delFinBankStatement(scope.row)" v-auth="'finBankStatement:delete'"> 删除 </el-button>
          </template>
        </el-table-column>
      </el-table>
      <el-pagination 
              v-model:currentPage="state.tableParams.page"
              v-model:page-size="state.tableParams.pageSize"
              @size-change="(val: any) => handleQuery({ pageSize: val })"
              @current-change="(val: any) => handleQuery({ page: val })"
              layout="total, sizes, prev, pager, next, jumper"
              :page-sizes="[10, 20, 50, 100, 200, 500]"
              :total="state.tableParams.total"
              size="small"
              background />
      <ImportData ref="importDataRef" :import="finBankStatementApi.importData" :download="finBankStatementApi.downloadTemplate" v-auth="'finBankStatement:import'" @refresh="handleQuery"/>
