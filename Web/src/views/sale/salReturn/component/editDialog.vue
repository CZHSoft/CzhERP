<script lang="ts" name="salReturn" setup>
import { ref, reactive, onMounted } from "vue";
import { ElMessage } from "element-plus";
import type { FormRules } from "element-plus";
import { formatDate } from '/@/utils/formatTime';
import { useSalReturnApi } from '/@/api/sale/salReturn';
//父级传递来的函数，用于回调
const emit = defineEmits(["reloadTable"]);
const salReturnApi = useSalReturnApi();
const ruleFormRef = ref();

const state = reactive({
	title: '',
	loading: false,
	showDialog: false,
	ruleForm: {} as any,
	stores: {},
	dropdownData: {} as any,
});

// 自行添加其他规则
const rules = ref<FormRules>({
  returnNo: [{required: true, message: '请选择退货单号！', trigger: 'blur',},],
  customerId: [{required: true, message: '请选择客户ID！', trigger: 'blur',},],
  customerName: [{required: true, message: '请选择客户名称！', trigger: 'blur',},],
  returnDate: [{required: true, message: '请选择退货日期！', trigger: 'change',},],
  totalQuantity: [{required: true, message: '请选择退货总数量！', trigger: 'blur',},],
  totalAmount: [{required: true, message: '请选择退货总金额！', trigger: 'blur',},],
  status: [{required: true, message: '请选择状态！', trigger: 'blur',},],
});

// 页面加载时
onMounted(async () => {
});

// 打开弹窗
const openDialog = async (row: any, title: string) => {
	state.title = title;
	row = row ?? {  };
	state.ruleForm = row.id ? await salReturnApi.detail(row.id).then(res => res.data.result) : JSON.parse(JSON.stringify(row));
	state.showDialog = true;
};

// 关闭弹窗
const closeDialog = () => {
	emit("reloadTable");
	state.showDialog = false;
};

// 提交
const submit = async () => {
	ruleFormRef.value.validate(async (isValid: boolean, fields?: any) => {
		if (isValid) {
			let values = state.ruleForm;
			await salReturnApi[state.ruleForm.id ? 'update' : 'add'](values);
			closeDialog();
		} else {
			ElMessage({
				message: `表单有${Object.keys(fields).length}处验证失败，请修改后再提交`,
				type: "error",
			});
		}
	});
};

//将属性或者函数暴露给父组件
defineExpose({ openDialog });
</script>
