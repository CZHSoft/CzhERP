<script lang="ts" name="salCredit" setup>
import { ref, reactive, onMounted } from "vue";
import { ElMessage } from "element-plus";
import type { FormRules } from "element-plus";
import { formatDate } from '/@/utils/formatTime';
import { useSalCreditApi } from '/@/api/sale/salCredit';
//父级传递来的函数，用于回调
const emit = defineEmits(["reloadTable"]);
const salCreditApi = useSalCreditApi();
const ruleFormRef = ref();

const state = reactive({
	title: '',
	loading: false,
	showDialog: false,
	ruleForm: {} as any,
	stores: {},
	dropdownData: {} as any,
});

// 自行添加其他规则
const rules = ref<FormRules>({
  customerId: [{required: true, message: '请选择客户ID！', trigger: 'blur',},],
  creditLevel: [{required: true, message: '请选择信用等级！', trigger: 'blur',},],
  creditLimit: [{required: true, message: '请选择信用额度！', trigger: 'blur',},],
  creditPeriod: [{required: true, message: '请选择信用期限(天)！', trigger: 'blur',},],
  usedAmount: [{required: true, message: '请选择已用额度！', trigger: 'blur',},],
  overdueCount: [{required: true, message: '请选择逾期次数！', trigger: 'blur',},],
});

// 页面加载时
onMounted(async () => {
});

// 打开弹窗
const openDialog = async (row: any, title: string) => {
	state.title = title;
	row = row ?? {  };
	state.ruleForm = row.id ? await salCreditApi.detail(row.id).then(res => res.data.result) : JSON.parse(JSON.stringify(row));
	state.showDialog = true;
};

// 关闭弹窗
const closeDialog = () => {
	emit("reloadTable");
	state.showDialog = false;
};

// 提交
const submit = async () => {
	ruleFormRef.value.validate(async (isValid: boolean, fields?: any) => {
		if (isValid) {
			let values = state.ruleForm;
			await salCreditApi[state.ruleForm.id ? 'update' : 'add'](values);
			closeDialog();
		} else {
			ElMessage({
				message: `表单有${Object.keys(fields).length}处验证失败，请修改后再提交`,
				type: "error",
			});
		}
	});
};

//将属性或者函数暴露给父组件
defineExpose({ openDialog });
</script>
<template>
	<div class="salCredit-container">
		<el-dialog v-model="state.showDialog" :width="800" draggable :close-on-click-modal="false">
			<template #header>
				<div style="color: #fff">
					<span>{{ state.title }}</span>
				</div>
			</template>
			<el-form :model="state.ruleForm" ref="ruleFormRef" label-width="auto" :rules="rules">
				<el-row :gutter="35">
					<el-form-item v-show="false">
						<el-input v-model="state.ruleForm.id" />
					</el-form-item>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="客户ID" prop="customerId">
							<el-input v-model="state.ruleForm.customerId" placeholder="请输入客户ID" show-word-limit clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="信用等级" prop="creditLevel">
							<el-input v-model="state.ruleForm.creditLevel" placeholder="请输入信用等级" maxlength="20" show-word-limit clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="信用额度" prop="creditLimit">
							<el-input-number v-model="state.ruleForm.creditLimit" placeholder="请输入信用额度" clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="信用期限(天)" prop="creditPeriod">
							<el-input-number v-model="state.ruleForm.creditPeriod" placeholder="请输入信用期限(天)" clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="已用额度" prop="usedAmount">
							<el-input-number v-model="state.ruleForm.usedAmount" placeholder="请输入已用额度" clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="逾期次数" prop="overdueCount">
							<el-input-number v-model="state.ruleForm.overdueCount" placeholder="请输入逾期次数" clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="最后逾期日期" prop="lastOverdueDate">
							<el-date-picker v-model="state.ruleForm.lastOverdueDate" type="date" placeholder="最后逾期日期" />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="评估日期" prop="assessDate">
							<el-date-picker v-model="state.ruleForm.assessDate" type="date" placeholder="评估日期" />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="评估人ID" prop="assessUserId">
							<el-input v-model="state.ruleForm.assessUserId" placeholder="请输入评估人ID" show-word-limit clearable />
						</el-form-item>
					</el-col>
						<el-col :xs="24" :sm="12" :md="12" :lg="12" :xl="12" class="mb20" >
						<el-form-item label="备注" prop="remark">
							<el-input v-model="state.ruleForm.remark" placeholder="请输入备注" maxlength="500" show-word-limit clearable />
						</el-form-item>
					</el-col>
				</el-row>
			</el-form>
			<template #footer>
				<span class="dialog-footer">
					<el-button @click="() => state.showDialog = false">取 消</el-button>
					<el-button @click="submit" type="primary" v-reclick="1000">确 定</el-button>
				</span>
			</template>
		</el-dialog>
	</div>
</template>
<style lang="scss" scoped>
:deep(.el-select), :deep(.el-input-number) {
  width: 100%;
}
</style>